WB_TO_ISO2: dict[str, str] = {v: k for k, v in ISO2_TO_WB.items()}

WB_INDICATORS: dict[str, str] = {
    "FP.CPI.TOTL.ZG": "inflation_pct",
    "NY.GDP.MKTP.KD.ZG": "gdp_growth_pct",
    "SL.UEM.TOTL.ZS": "unemployment_pct",
    "NY.GDP.MKTP.CD": "gdp_usd_billions",
    "GC.DOD.TOTL.GD.ZS": "government_debt_pct_gdp",
    "BN.CAB.XOKA.GD.ZS": "current_account_pct_gdp",
}

DEFAULT_WB_INDICATORS: dict[str, str] = WB_INDICATORS


def iso2_to_wb(country_code: str) -> str | None:
    """Resolve Velora ISO2 code to World Bank country ID (ISO3)."""
    code = (country_code or "").upper().strip()
    if len(code) == 3 and code in WB_TO_ISO2:
        return code
    return ISO2_TO_WB.get(code)